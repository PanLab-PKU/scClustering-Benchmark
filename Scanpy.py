import os
import pandas as pd
import scanpy as sc
import anndata as ad
import time
import gc
import psutil
import random

func="scanpy"

# 输入和输出路径
dir_use = "/home/ziyinghuang/project/survey/dataset/"
dir_out = "/home/ziyinghuang/project/survey/output/"
sets = [
    "E-MTAB-3321", "Klein",
    "GSE189120",
    "GSE171993", "Zeisel", "PBMC",  # previous
    "fig11981034",
    "GSE137537", "GSE109816","E-MTAB-6701/Placenta", "E-MTAB-6701/Decidua",
    "GSE125970/Rectum", "GSE125970/Colon", "GSE125970/Ileum",  # other tissue
    "Muris/FACS/Pancreas", "Muris/FACS/Fat", "Muris/drop/muscle", "Muris/drop/lung", 
    "Sapiens/Tongue", "Sapiens/Prostate",  # downsampling
    "NHP",
    "NM/14", "NM/19", "NM/20", "NM/29", "NM/39", "NM/41", "NM/57", "NM/58", 
    "NM/60", "NM/63","NG/A13", "NG/A30", "NG/E1", "NG/E2", "NG/E3",
    "Cao"
]

# 用于记录内存使用情况
memory = pd.DataFrame()
errors = []

for set_name in sets:
    
    print(f"Processing {set_name}...", flush=True)

    # 设置工作目录和输出目录
    wd = os.path.join(dir_use, set_name)
    out = os.path.join(dir_out, set_name)
    if not os.path.exists(out):
        os.makedirs(out)

    data = sc.read_10x_mtx(wd)
    sc.pp.filter_genes(data, min_cells=3)
    
    # 读取标签
    labels = pd.read_csv(os.path.join(wd, "labels.csv"))
    num_labels = len(labels.iloc[:, 1].unique())

    print(f"Number of labels {num_labels}...", flush=True)
    
    # 二分查找初始分辨率
    # for GSE189120
    r_min = 0.002
    r_max = 0.008
    r = 0.002

    # for NM58/NM63
    #r_min = 0.1
    #r_max = 1
    #r = 0.1

    # for fig11981034
    #r_min = 0.8
    #r_max = 1
    #r = 0.8

    clusters = pd.Series()

    # clustering
    random.seed(999)
    os.environ['PYTHONHASHSEED'] = '0'
    print(f"random seed {random.random()}")
    
    data.layers["counts"] = data.X.copy()
    sc.pp.normalize_total(data)
    sc.pp.log1p(data)
    sc.pp.highly_variable_genes(data, n_top_genes=2000)
    sc.tl.pca(data)
    sc.pp.neighbors(data)
    sc.tl.umap(data)

    # 初步聚类
    def run_clustering(data, resolution):
        sc.tl.leiden(data, flavor="igraph", n_iterations=2, resolution=resolution)
        return data.obs['leiden']

    # 计算初步聚类结果
    initial_clusters = run_clustering(data, r_min)
    num_clusters_min = len(initial_clusters.unique())

    initial_clusters = run_clustering(data, r_max)
    num_clusters_max = len(initial_clusters.unique())

    if num_labels >= num_clusters_min and num_labels <= num_clusters_max:
        # 目标聚类数量在r_min和r_max之间，可以继续进行二分查找
        max_iter = 2000  # 最大迭代次数
        iter_count = 0
        while len(clusters.unique()) != num_labels and iter_count < max_iter:
            
            if len(clusters.unique()) > num_labels:
                r_max = r
            elif len(clusters.unique()) < num_labels:
                r_min = r
            r = (r_min + r_max) / 2

            # 重新运行聚类
            random.seed(999)
            os.environ['PYTHONHASHSEED'] = '0'

            print(f"random seed {random.random()}")
            clusters = run_clustering(data, r)
            iter_count += 1
            res_chose = r

            print(f"Iteration {iter_count}, Resolution: {r}, Clusters: {len(clusters.unique())}", flush=True)
            print(f"Condition 1 (len(clusters.unique()) != num_labels): {len(clusters.unique()) != num_labels}", flush=True)
            print(f"Condition 2 (iter_count < max_iter): {iter_count < max_iter}", flush=True)

            if iter_count >= max_iter:
                print(f"Warning: Reached max iterations ({max_iter}) without converging to the desired num_labels.", flush=True)
    else:
        print(f"Warning: num_labels ({num_labels}) is not within the range of clusters generated by r_min ({num_clusters_min}) and r_max ({num_clusters_max}).", flush=True)
        res_chose = r_min  

    print(f"iteration {iter_count}", flush=True)
    print(f"final resolution {res_chose}", flush=True)

    try:
        # 内存和时间记录（前）
        start_time = time.time()
        
        # 重复读取数据
        wd = os.path.join(dir_use, set_name)
        data = sc.read_10x_mtx(wd)
        
        out = os.path.join(dir_out, set_name.strip('/'))
        if not os.path.exists(out):
            os.makedirs(out)
        print(f"Output path: {out}", flush=True)
        
        labels = pd.read_csv(os.path.join(wd, "labels.csv"))
        num_labels = len(labels.iloc[:, 1].unique())
        clusters = pd.Series()

        print(f"reloading labels {num_labels}", flush=True)
        
        # 聚类操作
        random.seed(999)
        os.environ['PYTHONHASHSEED'] = '0'
        print(f"random seed {random.random()}")
        
        data.layers["counts"] = data.X.copy()
        sc.pp.normalize_total(data)
        sc.pp.log1p(data)
        sc.pp.highly_variable_genes(data, n_top_genes=2000)
        sc.tl.pca(data)
        sc.pp.neighbors(data)
        sc.tl.umap(data)
        
        random.seed(999)
        os.environ['PYTHONHASHSEED'] = '0'
        print(f"random seed {random.random()}")

        clusters = run_clustering(data, res_chose)
        embed = data.obsm['X_pca']

        elapsed_time = time.time() - start_time

        time_df = pd.DataFrame({
        "Dataset": [set_name],
        "elapsed_time_seconds": [elapsed_time]
        })

        # 打印结果
        print(f"{set_name} finished.", flush=True)
        print(f"{set_name} origin labels: {num_labels}", flush=True)
        print(f"{set_name} labels: {len(clusters.unique())}", flush=True)
        print(f"{set_name} resolution: {res_chose}", flush=True)
        
        # 保存聚类和嵌入
        clusters.to_csv(os.path.join(out, f"cluster_{func}_res_{res_chose}_{time.strftime('%Y-%m-%d')}.csv"))
        pd.DataFrame(embed).to_csv(os.path.join(out, f"embedding_{func}_res_{res_chose}_{time.strftime('%Y-%m-%d')}.csv"),index=False)

        file_name = f"{out}/time_{func}_res_{res_chose}_{time.strftime('%Y-%m-%d')}.csv"
        time_df.to_csv(file_name, index=False)

    
    except Exception as e:
        errors.append({"set": set_name, "error": str(e)})
